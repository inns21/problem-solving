n = int(input())

for i in range(n-1):
  if i == 0:
    print('*'*n + " "*(n*2-3) + '*'*n)
  else:
    print(' '*i + '*'+  ' '*(n-2) + '*' + ' '*(n*2-(3+(i*2))) + '*' + ' '*(n-2) + '*')

print(' '*(n-1)+ '*' + ' '*(n-2) + '*' + ' '*(n-2) + '*')

for i in range(n-2,-1,-1):
  if i == 0:
    print('*'*n + " "*(n*2-3) + '*'*n)
  else:
    print(' '*i + '*'+  ' '*(n-2) + '*' + ' '*(n*2-(3+(i*2))) + '*' + ' '*(n-2) + '*')
